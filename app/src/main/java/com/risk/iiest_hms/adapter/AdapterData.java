package com.risk.IIEST_HMS.Adapter;


public class AdapterData {
    public String mLedger;
    public String mDate;
    public String mFine;
    public String mReceiptLink;

    public AdapterData(String mLedger, String mDate, String mFine, String mReceiptLink) {
        this.mLedger = mLedger;
        this.mDate = mDate;
        this.mFine = mFine;
        this.mReceiptLink = mReceiptLink;
    }
}
